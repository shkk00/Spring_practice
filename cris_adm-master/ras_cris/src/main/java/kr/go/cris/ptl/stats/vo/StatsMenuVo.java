package kr.go.cris.ptl.stats.vo;

import java.io.Serializable;

import kr.go.cris.commons.vo.CommonsVo;

import org.apache.ibatis.type.Alias;


@Alias("statsMenuVo")
public class StatsMenuVo extends CommonsVo  implements Serializable  {

	private static final long serialVersionUID = 1L;
	
	/** 시퀀스NO */
	private int no_seq;
	
	/** 사용자IP */
	private String user_ip;
	
	/** 메뉴타입 */
	private String menu_type;
	
	/** 메뉴URI */
	private String menu_uri;
	
	/** 등록일 */
	private String reg_date;

	public int getNo_seq() {
		return no_seq;
	}

	public void setNo_seq(int no_seq) {
		this.no_seq = no_seq;
	}

	public String getUser_ip() {
		return user_ip;
	}

	public void setUser_ip(String user_ip) {
		this.user_ip = user_ip;
	}

	public String getMenu_type() {
		return menu_type;
	}

	public void setMenu_type(String menu_type) {
		this.menu_type = menu_type;
	}

	public String getMenu_uri() {
		return menu_uri;
	}

	public void setMenu_uri(String menu_uri) {
		this.menu_uri = menu_uri;
	}

	public String getReg_date() {
		return reg_date;
	}

	public void setReg_date(String reg_date) {
		this.reg_date = reg_date;
	}

	

}
